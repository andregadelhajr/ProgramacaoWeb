// Declare duas variáveis numéricas para cada operador
var num_A = 5;
var num_B = 3;

// Realize as operações e guarde os resultados em uma terceira variável
var soma = num_A + num_B;
var subtracao = num_A - num_B;
var multiplicacao = num_A * num_B;
var divisao = num_A / num_B;

// Imprima os resultados
console.log(`Resultado da soma entre ${num_A} e ${num_B} = ${soma}`);
console.log(`Resultado da subtração entre ${num_A} e ${num_B} = ${subtracao}`);
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} = ${multiplicacao}`);
console.log(`Resultado da divisão entre ${num_A} e ${num_B} = ${divisao}`);
